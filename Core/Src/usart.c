static void bubble_sort(float arr[], uint8_t len) {
    int i, j;
    for (i = 0; i < len - 1; i++) {
        for (j = 0; j < len - 1 - i; j++) {
            if (arr[j] > arr[j+1]) {
                int temp = arr[j];
                arr[j] = arr[j+1];
                arr[j+1] = temp;
            }
        }
    }
}

static void Swap(float * a,float * b)
{
	float  temp=*a;
	*a=*b;
	*b=temp;
}

// 冒泡排序
void BubbleSort(float* a, int n)
{

	int i = 0;
	int flag = 0;

    //n-1趟排序
	for (i = 0; i < n-1; i++)
	{
		int j = 0;

        //一趟冒泡排序
		for (j = 0; j < n - i - 1; j++)
		{
			if (a[j] > a[j+1])
			{
				Swap(&a[j], &a[j+1]);
				flag = 1;
			}
		}

        //若某一趟排序中没有元素交换则说明所有元素已经有序，不需要再排序
		if (flag == 0)
		{
			break;
		}
	}
}

